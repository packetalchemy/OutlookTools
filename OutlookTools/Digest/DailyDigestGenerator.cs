using System;
using System.Collections.Generic;
using System.Drawing;
using System.Linq;
using System.Windows.Forms;
using Outlook = Microsoft.Office.Interop.Outlook;

namespace OutlookTools.Digest
{
    /// <summary>
    /// OutlookTools — Daily Digest Generator
    /// Generates a morning summary of:
    /// - New emails since last check
    /// - Pending follow-ups
    /// - Unread counts
    /// - Top senders of the day
    /// - Flagged items
    /// 
    /// Output: On-screen dashboard (optionally auto-send as email).
    /// </summary>
    public static class DailyDigestGenerator
    {
        /// <summary>
        /// Generate digest for the current day.
        /// </summary>
        public static DigestData Generate(Outlook.Application app)
        {
            var data = new DigestData();
            Outlook.NameSpace session = app.Session;

            try
            {
                // --- Inbox stats ---
                Outlook.Folder inbox = session.GetDefaultFolder(
                    Outlook.OlDefaultFolders.olFolderInbox) as Outlook.Folder;
                if (inbox != null)
                {
                    data.TotalUnread = inbox.UnReadItemCount;
                    data.TodayNew = CountTodayEmails(inbox);
                    data.TodaySenders = GetTopSendersToday(inbox);
                    data.FlaggedCount = CountFlagged(inbox);
                }

                // --- Sent stats ---
                Outlook.Folder sent = session.GetDefaultFolder(
                    Outlook.OlDefaultFolders.olFolderSentMail) as Outlook.Folder;
                if (sent != null)
                {
                    data.SentToday = CountTodaySent(sent);
                }

                // --- Follow-up pending ---
                data.PendingFollowUps = FollowUp.FollowUpTracker.GetPending().Count;

                // --- Overdue follow-ups ---
                data.OverdueFollowUps = FollowUp.FollowUpTracker.GetPending()
                    .Count(x => x.Status == FollowUp.FollowUpStatus.Overdue);

                data.GeneratedAt = DateTime.Now;
            }
            catch (Exception ex)
            {
                ThisAddIn.LogDebug("DailyDigest.Generate: " + ex.Message);
            }

            return data;
        }

        /// <summary>
        /// Format digest as HTML email body.
        /// </summary>
