        // === Helpers ===

        private static int CountTodayEmails(Outlook.Folder folder)
        {
            int count = 0;
            try
            {
                Outlook.Items items = folder.Items;
                items.Sort("[ReceivedTime]", true);
                var today = DateTime.Today;

                foreach (object obj in items)
                {
                    if (!(obj is Outlook.MailItem mail)) continue;
                    try
                    {
                        if (mail.ReceivedTime < today) break;
                        count++;
                    }
                    finally { ReleaseCom(mail); }
                }
            }
            catch { }
            return count;
        }

        private static int CountTodaySent(Outlook.Folder folder)
        {
            int count = 0;
            try
            {
                Outlook.Items items = folder.Items;
                items.Sort("[SentOn]", true);
                var today = DateTime.Today;

                foreach (object obj in items)
                {
                    if (!(obj is Outlook.MailItem mail)) continue;
                    try
                    {
                        if (mail.SentOn < today) break;
                        count++;
                    }
                    finally { ReleaseCom(mail); }
                }
            }
            catch { }
            return count;
        }

        private static int CountFlagged(Outlook.Folder folder)
        {
            int count = 0;
            try
            {
                foreach (object obj in folder.Items)
                {
                    if (!(obj is Outlook.MailItem mail)) continue;
                    try
                    {
                        if (mail.FlagStatus != Outlook.OlFlagStatus.olNoFlag)
                            count++;
                    }
                    finally { ReleaseCom(mail); }
                }
            }
            catch { }
            return count;
        }

        private static Dictionary<string, int> GetTopSendersToday(Outlook.Folder folder)
        {
            var senders = new Dictionary<string, int>();
            try
            {
                Outlook.Items items = folder.Items;
                items.Sort("[ReceivedTime]", true);
                var today = DateTime.Today;

                foreach (object obj in items)
                {
                    if (!(obj is Outlook.MailItem mail)) continue;
                    try
                    {
                        if (mail.ReceivedTime < today) break;
                        string name = mail.SenderName ?? "Unknown";
                        if (senders.ContainsKey(name))
                            senders[name]++;
                        else
                            senders[name] = 1;
                    }
                    finally { ReleaseCom(mail); }
                }
            }
            catch { }

            return senders.OrderByDescending(x => x.Value)
                .Take(5)
                .ToDictionary(x => x.Key, x => x.Value);
        }

        private static void ReleaseCom(object obj)
        {
            try
            {
                if (obj != null && System.Runtime.InteropServices.Marshal.IsComObject(obj))
                    System.Runtime.InteropServices.Marshal.ReleaseComObject(obj);
            }
            catch { }
        }
    }

    public class DigestData
    {
        public int TodayNew { get; set; }
        public int SentToday { get; set; }
        public int TotalUnread { get; set; }
        public int FlaggedCount { get; set; }
        public int PendingFollowUps { get; set; }
        public int OverdueFollowUps { get; set; }
        public Dictionary<string, int> TodaySenders { get; set; } = new Dictionary<string, int>();
        public DateTime GeneratedAt { get; set; }
    }
}
